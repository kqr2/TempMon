/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  //MX_CRC_Init();
  //MX_DMA2D_Init();
  //MX_FMC_Init();
  //MX_I2C3_Init();
  //MX_LTDC_Init();
  //MX_SPI5_Init();
  //MX_TIM1_Init();
  MX_USART1_UART_Init();
  MX_USB_HOST_Init();
  //MX_ADC1_Init();
  MX_FATFS_Init();
  /* USER CODE BEGIN 2 */

  // https://embedded.fm/blog/2017/1/19/discovery-uart-input
  // Disable buffering of serial ports
  setvbuf(stdin, NULL, _IONBF, 0);
  setvbuf(stdout, NULL, _IONBF, 0);

  // Initialize command console
  ConsoleInit();

  // Initialize LCD
  BSP_LCD_Init();

#define LCD_FRAME_BUFFER_LAYER0                  (LCD_FRAME_BUFFER+0x130000)
#define LCD_FRAME_BUFFER_LAYER1                  LCD_FRAME_BUFFER

  /* Layer2 Init */
  BSP_LCD_LayerDefaultInit(1, LCD_FRAME_BUFFER_LAYER1);
  /* Set Foreground Layer */
  BSP_LCD_SelectLayer(1);

  /* Clear the LCD */
  BSP_LCD_Clear(LCD_COLOR_WHITE);
  BSP_LCD_SetColorKeying(1, LCD_COLOR_WHITE);
  BSP_LCD_SetLayerVisible(1, DISABLE);

  /* Layer1 Init */
  BSP_LCD_LayerDefaultInit(0, LCD_FRAME_BUFFER_LAYER0);
  /* Set Foreground Layer */
  BSP_LCD_SelectLayer(0);

  /* Enable The LCD */
  BSP_LCD_DisplayOn();

  /* Clear the LCD */
  BSP_LCD_Clear(LCD_COLOR_WHITE);

  BSP_LCD_SetTextColor(LCD_COLOR_DARKGRAY);
  BSP_LCD_SetFont(&Font16);

  /* Initialize user button to cause an interrupt */
  BSP_PB_Init(BUTTON_KEY, BUTTON_MODE_EXTI);
  button_pressed = BSP_PB_GetState(BUTTON_KEY);
  button_last = button_pressed;

  sys_init(&sys);
  RV8803 *rtc = &sys.rtc;
  tempmon_state_t state = TEMPMON_STATE_MONITOR;

  uint32_t temp;
  uint8_t temp_buf[32];
  uint32_t ticks = 0;
  uint32_t samples = 0;

  ApplicationTypeDef Last_appli_state = APPLICATION_IDLE;
  char fname[64];
  uint32_t byteswritten;
  char *newline = "\r\n";
  int nl = strlen(newline);
  FRESULT res;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */
    ConsoleProcess();

    /* Process button */
    if (button_interrupt) {
      // read the current state of the switch into a local variable:
      int button = BSP_PB_GetState(BUTTON_KEY);
      
      if (button != button_last) {
	button_debounce_cntr = BUTTON_DEBOUNCE_COUNT;
      } else {
	button_debounce_cntr--;
      }
      button_last = button;
      
      if (button_debounce_cntr <= 0) {
	// Buton debounce
	button_pressed = button;
	button_interrupt = false;
      }
    }

    // Process next state
    if (button_pressed) {
      BSP_LCD_Clear(LCD_COLOR_WHITE);
      
      switch (state) {
      case TEMPMON_STATE_MONITOR:
	if (FatFS_opened()) {
	  FatFS_close();
	  BSP_LCD_DisplayStringAtLine(1, "TEMPMON LOG FILE  ");
	  BSP_LCD_DisplayStringAtLine(2, fname);
	} else {
	  BSP_LCD_DisplayStringAtLine(1, "INSERT FLASH DRIVE");
	}
	Last_appli_state = Appli_state;
	state = TEMPMON_STATE_IDLE;
	break;
      case TEMPMON_STATE_IDLE:
	state = TEMPMON_STATE_SCAN;
	samples = 0;
	break;
      case TEMPMON_STATE_SCAN:
	state = TEMPMON_STATE_MONITOR;
	samples = 0;
	break;
      default:
	break;
      }
    }
    
    if (button_pressed || (ticks % 500 == 0))  {
      int line = 1;

      switch (state) {
      case TEMPMON_STATE_MONITOR:
	rtc->updateTime();
	BSP_LCD_SPRINTF(line++, temp_buf, "%s %s",
			rtc->stringDateUSA(), rtc->stringTime());

	if (!FatFS_opened()) {
	  sprintf(fname, "TM_%04d%02d%02d_%02d%02d.TXT",
		  rtc->getYear(), rtc->getMonth(), rtc->getDate(),
		  rtc->getHours(), rtc->getMinutes());
	  FatFS_open(fname);
	}
	BSP_LCD_SPRINTF(line++, temp_buf, "LOGGING: %s",
			FatFS_opened() ? "ON " : "OFF");
	
	BSP_LCD_SPRINTF(line++, temp_buf, "Samples: %u", samples++);

	if (FatFS_opened()) {
	  res = f_write(&USBHFile, temp_buf, strlen(temp_buf), (void *)&byteswritten);
	  res = f_write(&USBHFile, newline, nl, (void *)&byteswritten);
	}

	for (int k=0; k<TMP102_MAX_SENSORS; k++) {
	  tmp102_t *tmp = &sys.tmp[k];
	  if (tmp->detect) {
	    temp = tmp102_read_temp(tmp);
	    temp *= 625;
	    BSP_LCD_SPRINTF(line++, temp_buf,
			    "Temp 0x%02x : %u.%u", tmp->addr, temp/10000, temp % 10000);
	    if (FatFS_opened()) {
	      res = f_write(&USBHFile, temp_buf, strlen(temp_buf), (void *)&byteswritten);
	      res = f_write(&USBHFile, newline, nl, (void *)&byteswritten);
	    }
	  }
	}
	break;

      case TEMPMON_STATE_IDLE:
	if (Appli_state != Last_appli_state) {
	  BSP_LCD_Clear(LCD_COLOR_WHITE);
	  switch (Appli_state) {
	  case APPLICATION_READY:
	    BSP_LCD_DisplayStringAtLine(1, "FLASH DRIVE READY");
	    break;
	  case APPLICATION_DISCONNECT:
	    BSP_LCD_DisplayStringAtLine(1, "INSERT FLASH DRIVE");
	    break;
	  default:
	    break;
	  }
	  Last_appli_state = Appli_state;
	}
	break;      
      
      case TEMPMON_STATE_SCAN:
	// Re-scan
	if (sys_tmp_rescan(&sys)) {
	  BSP_LCD_Clear(LCD_COLOR_WHITE);
	}
	BSP_LCD_SPRINTF(line++, temp_buf, "Scanning: %u", samples++);
	for (int k=0; k<TMP102_MAX_SENSORS; k++) {
	  tmp102_t *tmp = &sys.tmp[k];
	  if (tmp->detect) {
	    BSP_LCD_SPRINTF(line++, temp_buf, "Temp 0x%02x detected", tmp->addr);
	  }
	}
	if (sys.ntmp == 0) {
	  BSP_LCD_DisplayStringAtLine(line, "Please attach sensors");
	}
	break;

      default:
	break;      
      }
    }

    button_pressed = 0;

    // Timer tick
    HAL_Delay(2);
    ticks++;
  }
  /* USER CODE END 3 */
}


/**
  * @brief  System Clock Configuration
  *         The system Clock is configured as follow :
  *            System Clock source            = PLL (HSE)
  *            SYSCLK(Hz)                     = 168000000
  *            HCLK(Hz)                       = 168000000
  *            AHB Prescaler                  = 1
  *            APB1 Prescaler                 = 4
  *            APB2 Prescaler                 = 2
  *            HSE Frequency(Hz)              = 8000000
  *            PLL_M                          = 8
  *            PLL_N                          = 336
  *            PLL_P                          = 2
  *            PLL_Q                          = 7
  *            VDD(V)                         = 3.3
  *            Main regulator output voltage  = Scale1 mode
  *            Flash Latency(WS)              = 5
  * @param  None
  * @retval None
  */
void SystemClock_Config(void)
{
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_OscInitTypeDef RCC_OscInitStruct;

  /* Enable Power Control clock */
  __HAL_RCC_PWR_CLK_ENABLE();

  /* The voltage scaling allows optimizing the power consumption when the device is
     clocked below the maximum system frequency, to update the voltage scaling value
     regarding system frequency refer to product datasheet.  */
  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

  /* Enable HSE Oscillator and activate PLL with HSE as source */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 8;
  RCC_OscInitStruct.PLL.PLLN = 336;
  RCC_OscInitStruct.PLL.PLLP = 2;
  RCC_OscInitStruct.PLL.PLLQ = 7;
  HAL_RCC_OscConfig(&RCC_OscInitStruct);

  /* Select PLL as system clock source and configure the HCLK, PCLK1 and PCLK2
     clocks dividers */
  RCC_ClkInitStruct.ClockType = (RCC_CLOCKTYPE_SYSCLK | RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_PCLK1 | RCC_CLOCKTYPE_PCLK2);
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;
  HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_5);
}


